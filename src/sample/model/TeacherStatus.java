package sample.model;

public class TeacherStatus {
    private String TeacherStatusCode;
    private String TeacherStatus;
    private String Notes;

    public String getTeacherStatusCode() {
        return TeacherStatusCode;
    }

    public void setTeacherStatusCode(String teacherStatusCode) {
        TeacherStatusCode = teacherStatusCode;
    }

    public String getTeacherStatus() {
        return TeacherStatus;
    }

    public void setTeacherStatus(String teacherStatus) {
        TeacherStatus = teacherStatus;
    }

    public String getNotes() {
        return Notes;
    }

    public void setNotes(String notes) {
        Notes = notes;
    }
}
